<?php

namespace TTBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * CmsSocialLikes
 *
 * @ORM\Table(name="cms_social_likes", indexes={@ORM\Index(name="channel_id", columns={"channel_id"}), @ORM\Index(name="single_like", columns={"user_id"})})
 * @ORM\Entity
 */
class CmsSocialLikes
